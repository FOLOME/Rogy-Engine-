void eraseSubStr(std::string& mainStr, std::string& SubStr)
{
	// find for the SubStr in mainStr
	size_t pos = mainStr.find(SubStr);

	if (pos != std::string::npos)
	{
		// if found erase it.
		mainStr.erase(pos, SubStr.length());
	}
}
/* ----------------------------------------------------------------------------- */
void eraseAllSubStr(std::string& mainStr, std::string& SubStr)
{
	size_t pos = std::string::npos;

	// search for the substr in loop intel nothing found.
	while ((pos = mainStr.find(SubStr)) != std::string::npos)
	{
		// if found erase it from string.
		mainStr.erase(pos, SubStr.length());
	}
}
/* ----------------------------------------------------------------------------- */
void RGetRelativePath(std::string& path)
{
	eraseSubStr(path, RGetCurrentPath() + "\\");
}
