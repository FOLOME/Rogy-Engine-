void EditGizmos(glm::mat4 matr, glm::vec3 &pos, glm::vec3 &Scale, glm::vec3 &rot, glm::mat4 view, glm::mat4 proj , ImGuizmo::OPERATION mCurrentGizmoOperation, ImGuizmo::MODE mCurrentGizmoMode)
{
	glm::mat4 gizmo_Mat = glm::mat4(1);
	gizmo_Mat = glm::translate(gizmo_Mat, pos);
	//gizmo_Mat *= glm::mat4_cast(glm::quat(glm::radians(rot)));
	gizmo_Mat = glm::scale(gizmo_Mat, Scale);

	float matrixTranslation[3], matrixRotation[3], matrixScale[3];

	float cameraView[16], cameraProjection[16], objectMatrix[16];

	float *gizmo_mat = (float*)glm::value_ptr(gizmo_Mat);
	const float *view_m = (const float*)glm::value_ptr(view);
	const float *pro_m = (const float*)glm::value_ptr(proj);

	for (int i = 0; i < 16; ++i)
	{
		cameraProjection[i] = pro_m[i];
		cameraView[i] = view_m[i];
		objectMatrix[i] = gizmo_mat[i];
	}

	//ImGuizmo::RecomposeMatrixFromComponents(objectMatrix, matrixTranslation, matrixRotation, matrixScale);
	ImGuizmo::Manipulate(cameraView, cameraProjection, mCurrentGizmoOperation, mCurrentGizmoMode, gizmo_mat, NULL, false);
	ImGuizmo::DecomposeMatrixToComponents(gizmo_mat, matrixTranslation, matrixRotation, matrixScale);

	pos = from_float4(matrixTranslation);
	Scale = from_float4(matrixScale);
	rot = from_float4(matrixRotation);
	
}
